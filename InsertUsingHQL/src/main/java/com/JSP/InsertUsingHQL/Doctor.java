package com.JSP.InsertUsingHQL;

public class Doctor {
	private int did;
	private String dname;
	private double dsal;

	public int getDid() {
		return did;
	}

	public void setDid(int did) {
		this.did = did;
	}

	public String getDname() {
		return dname;
	}

	public void setDname(String dname) {
		this.dname = dname;
	}

	public double getDsal() {
		return dsal;
	}

	public void setDsal(double dsal) {
		this.dsal = dsal;
	}

}
